async def chairman_response(user_text: str, context: list) -> str:
    """Стандартный ответ Chairman — вызывается при @упоминании или ответе на бота."""
    msgs = context + [{"role": "user", "content": user_text}]
    return await _call(CHAIRMAN_PROMPT, msgs, enable_search=should_search(user_text))


async def magi_council(
    query: str,
    context: list,
    on_complete=None   # async callable(name: str, result: str)
) -> dict:
    """
    Запускает трёх MAGI параллельно, затем Chairman синтезирует.
    on_complete вызывается по мере завершения каждого MAGI.
    Возвращает {"melchior", "balthasar", "caspar", "chairman"}.
    """
    results: dict[str, str] = {}
    search = should_search(query)

    async def _run(name: str, system: str):
        msgs = context + [{"role": "user", "content": query}]
        result = await _call(system, msgs, max_tokens=500, enable_search=search)
        results[name] = result
        if on_complete:
            await on_complete(name, result)

    await asyncio.gather(
        _run("melchior",  MELCHIOR_PROMPT),
        _run("balthasar", BALTHASAR_PROMPT),
        _run("caspar",    CASPAR_PROMPT),
    )

    # Chairman синтезирует
    synthesis_input = (
        f"Запрос: {query}\n\n"
        f"Melchior:\n{results['melchior']}\n\n"
        f"Balthasar:\n{results['balthasar']}\n\n"
        f"Caspar:\n{results['caspar']}"
    )
    results["chairman"] = await _call(
        SYNTHESIS_PROMPT,
        [{"role": "user", "content": synthesis_input}],
        max_tokens=600,
    )
    return results


async def vision_response(media_b64: str, media_type: str, caption: str,
                          context: list, is_video: bool = False) -> str:
    """Анализ фото или видео (до ~60с). Бесплатная модель с платным фоллбэком."""
    prompt = caption or ("Что происходит на этом видео? Опиши подробно."
                         if is_video else "Что изображено на этом фото? Опиши подробно.")

    media_block = (
        {"type": "video_url", "video_url": {"url": f"data:{media_type};base64,{media_b64}"}}
        if is_video else
        {"type": "image_url", "image_url": {"url": f"data:{media_type};base64,{media_b64}"}}
    )

    msgs = context + [{
        "role": "user",
        "content": [media_block, {"type": "text", "text": prompt}],
    }]

    result = await _call(CHAIRMAN_PROMPT, msgs, model=VISION_MODEL_FREE, max_tokens=700)

    # Бесплатная модель упёрлась в лимит или недоступна — пробуем платный фоллбэк
    if result.startswith("[Ошибка"):
        result = await _call(CHAIRMAN_PROMPT, msgs, model=VISION_MODEL_PAID, max_tokens=700)

    return result
