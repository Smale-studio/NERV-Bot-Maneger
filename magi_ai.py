# ══════════════════════════════════════════
#              WEB SEARCH HEURISTИКА
# ══════════════════════════════════════════

_SEARCH_TRIGGERS = (
    "найди", "поищи", "загугли", "погугли", "узнай", "что нового",
    "последние новости", "новости про", "актуальн", "сейчас в мире",
    "сегодняшн", "search", "google it", "look up", "what's new",
    "current price", "курс валют", "погода",
)


def should_search(text: str) -> bool:
    """Грубая эвристика: нужен ли веб-поиск для этого запроса."""
    low = text.lower()
    return any(trigger in low for trigger in _SEARCH_TRIGGERS)


# ══════════════════════════════════════════
#              API CALLS
# ══════════════════════════════════════════

async def _call(system: str, messages: list, model: str | None = None,
                max_tokens: int = 700, enable_search: bool = False) -> str:
    extra_body = {}
    if enable_search:
        extra_body["plugins"] = [{"id": "web", "max_results": 3}]
    try:
        resp = await client.chat.completions.create(
            model=model or DEEPSEEK_MODEL,
            messages=[{"role": "system", "content": system}] + messages,
            max_tokens=max_tokens,
            temperature=0.72,
            extra_body=extra_body or None,
        )
        return resp.choices[0].message.content.strip()
    except Exception as e:
        return f"[Ошибка: {e}]"


